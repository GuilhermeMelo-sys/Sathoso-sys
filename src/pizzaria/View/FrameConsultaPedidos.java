    private void ConfigurarTable() {
        AtualizarTable();
        try {
            controllerPedido.pegarVarios().forEach(c -> tbp.addRow(
                    new Object[]{c.getId(), c.getEndereco().getId(), c.getValorTotal(), c.getStatus()}
            ));
        } catch (RuntimeException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível abrur a tabela! \nMotivo:" + ex.getMessage());
        }

        tbPedidos.setModel(tbp);
    }

    private void ConfigurarTablePesquisa(int id) {
        VerificarValoresNumericos();
        AtualizarTable();

        try {
            controllerPedido.pegar(id).forEach(c -> tbpPesquisa.addRow(
                    new Object[]{c.getId(), c.getEndereco().getId(), c.getValorTotal(), c.getStatus()}
            ));

            tbPedidos.setModel(tbpPesquisa);

        } catch (RuntimeException ex) {
            txtId.setText("");
            ConfigurarTable();
        }
    }

    private void VerificarValoresNumericos() throws NumberFormatException {
        boolean numero = txtId.getText().chars().anyMatch(c -> !(Character.isDigit(c)));

        if (numero) {
            throw new NumberFormatException("Valores não númericos, não são aceitos!");
        }
    }

    private void ConfigurarTablePesquisa(boolean estado) {
        AtualizarTable();

        controllerPedido.pegar(estado).forEach(c -> tbpPesquisa.addRow(
                new Object[]{c.getId(), c.getEndereco().getId(),
                    c.getValorTotal(), c.getStatus()}
        ));
        tbPedidos.setModel(tbpPesquisa);
    }

