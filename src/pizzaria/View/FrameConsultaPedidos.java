    private void ColocandoPesquisaNaTabela() throws HeadlessException {
        if (!(txtId.getText().isEmpty())) {
            try {
                ConfigurarTablePesquisa(Integer.parseInt(txtId.getText()));
            } catch (NumberFormatException ex) {
                txtId.setText("");
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        } else {
            ConfigurarTable();
        }
    }

    private void AtualizarTable() {
        tbpPesquisa.deleteAll();
        tbp.deleteAll();
    }


